		;>2.0V  P10翻转   <2.0V P10口为低电平
			
		;LVD 低电压检测，设置电压检测点为2.9V
		;上电电压为5V，P00输出为高，电压往下调，<2.9V，P00输出为低，>2.9V，P00输出为高
